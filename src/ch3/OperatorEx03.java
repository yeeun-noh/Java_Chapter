package ch3;

public class OperatorEx03 {

	public static void main(String[] args) {
		
		int i = 5, j = 5;
		System.out.println(i++); // i=5
		System.out.println(i++); // i=6
		
		System.out.println(++j); // j=6
		System.out.println(++j); // j=7
		
		System.out.println("i= " + i + " and j= " + j);	// i=7, j=7	
	}

}
